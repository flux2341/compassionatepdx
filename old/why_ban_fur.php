  <h4>Why Ban Fur?</h4>

  <h5>It's Cruel to Animals</h5>
  <ul class="browser-default">
    <li>Each year, more than 1 billion animals are killed for their fur worldwide, including rabbits, mink, foxes, coyotes, chinchillas, rabbits, seals, dogs, and cats. Roughly 85% of animals killed for fur are raised on fur factory farms, with the remainder trapped in the wild.</li>
    <li>Animals killed on fur farms spend their entire lives in cramped cages. These species are wild, with territorial ranges in the hundreds of acres. The confinement and boredom often leads to cage psychosis - frantic pacing, self-mutilation, and cannibalism. If healthy, they'll meet a violent end at a fraction of their natural lifespan.</li>
    <li>Common methods of killing animals on fur farms are anal electrocution, gassing, suffocation, and neck-breaking, while trapped animals are often either shot or bludgeoned to death.</li>
    <li>Animals trapped in the wild are often stuck in traps for days, exposed to the elements, injured and bleeding, and without food or water. Traps are indiscriminate, sometimes killing companion animals and endangered species.</li>
  </ul>
  <h5>It's Damaging to the Environment</h5>
  <ul class="browser-default">
    <li>The fur industry contributes to water pollution through the discharge of nitrogen and phosphorous in feces. According to a Michigan State University study, the US mink industry alone adds almost 1,000 tons each year.</li>
    <li>Fur is processed with carcinogenic chemicals to prevent it from degrading, including chromium and formaldehyde.</li>
    <li>It takes fifteen times more energy to make a coat from animal fur than from faux fur. Faux fur can be made from recycled or biodegradable materials, or can simply be avoided.</li>
  </ul>
  <h5>It's Unnecessary</h5>
  <ul class="browser-default">
    <li>It’s a luxury item, worn for fashion and status. Coats with just fur trim often cost thousands. There are plenty of alternative ways to keep warm.</li>
    <li>A ban would have little impact on the local economy. Fewer than 10 stores regularly sell fur items, and those that do, it's an insignificant part of their overall sales.</li>
    <li>Joining other cities in prohibiting this archaic, cruel, and unnecessary product is in line with Portland’s progressive ethics.</li>
  </ul>
  <h5>It's Been Banned Successfully Before</h5>
  <ul class="browser-default">
    <li>Fur farming is banned in many European countries including the UK, Germany, Belgium, Norway, Austria, The Czech Republic, Slovenia, and Serbia. <a href="https://www.furfreealliance.com/fur-bans/">Fur Free Alliance</a></li>
    <li>The sale and manufacture of fur is banned in several US cities including Los Angeles, San Francisco, Berkeley, and West Hollywood.</li>
  </ul>
