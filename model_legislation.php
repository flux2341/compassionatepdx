



- A city ordinance (not a ballot initiative) decided by the four city commissioners and the mayor
- Prohibits the manufacture and sale of new fur products within city limits: fur coats, coats with fur trim, handbags, shoes, hats, and keychains.
- 1 year phase-in period for companies to sell current stock
- Does not cover the sale of used or vintage fur
- Does not prohibit people from wearing fur
- Does not cover the sale of fur from animals trapped within the state, which is managed by the Oregon Department of Fish and Wildlife
- Does not apply to federally recognized Native American tribes
